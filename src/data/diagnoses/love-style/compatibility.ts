/**
 * 特定タイプの全他タイプとの相性リストをスコア降順で返す
 */
export function getAllPairsForType(
  typeId: LoveStyleType
): Array<{ typeId: LoveStyleType; compatibility: CompatibilityData }> {
  const allTypes: LoveStyleType[] = ['eros', 'ludus', 'storge', 'pragma', 'mania', 'agape'];
  return allTypes
    .filter((t) => t !== typeId)
    .map((t) => ({
      typeId: t,
      compatibility: getCompatibility(typeId, t),
    }))
    .sort((a, b) => b.compatibility.score - a.compatibility.score);
}
