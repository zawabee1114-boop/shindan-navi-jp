import type { MBTIType, TemperamentId } from './meta';
export type { MBTIType };

// meta.ts に TemperamentId を追加するのではなく groups.ts から import する設計
// ここでは直接定義して再利用
type TemperamentType = 'NT' | 'NF' | 'SJ' | 'SP';

export interface FunctionStack {
  /** 主機能（dominant）- 最も発達した認知機能 */
  dominant: string;
  /** 補助機能（auxiliary） */
  auxiliary: string;
  /** 三次機能（tertiary） */
  tertiary: string;
  /** 劣等機能（inferior）- 最も未発達・無意識 */
  inferior: string;
}

export interface Big5Profile {
  /** 開放性（Openness）0-100 ※ユーザー向け非表示 */
  O: number;
  /** 誠実性（Conscientiousness）0-100 ※ユーザー向け非表示 */
  C: number;
  /** 外向性（Extraversion）0-100 ※ユーザー向け非表示 */
  E: number;
  /** 協調性（Agreeableness）0-100 ※ユーザー向け非表示 */
  A: number;
  /** 神経症傾向（Neuroticism）0-100 ※ユーザー向け非表示 */
  N: number;
}

export interface MBTITypeDetail {
  /** タイプID */
  id: MBTIType;
  /** 日本語タイプ名（Keirsey命名ベース） */
  name: string;
  /** ニックネーム（通称） */
  nickname: string;
  /** Beebe理論の機能スタック */
  functionStack: FunctionStack;
  /** Keirsey 4気質 */
  temperament: TemperamentType;
  /** タイプ説明（300-400字） */
  description: string;
  /** 強み（4項目） */
  strengths: string[];
  /** 注意点（4項目）*/
  weaknesses: string[];
  /** 仕事傾向（150字） */
  workStyle: string;
  /** 恋愛傾向（150字） */
  loveStyle: string;
  /** 友人関係傾向（150字） */
  friendStyle: string;
  /** Big5換算傾向値（研究傾向値・個人差あり・ユーザー向け非表示） */
  big5: Big5Profile;
}

