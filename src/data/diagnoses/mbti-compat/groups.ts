/**
 * タイプから気質グループを取得する
 */
export function getTemperament(type: MBTIType): TemperamentId {
  for (const [id, group] of Object.entries(temperamentGroups)) {
    if ((group.types as readonly MBTIType[]).includes(type)) {
      return id as TemperamentId;
    }
  }
  return 'NT'; // fallback
}
