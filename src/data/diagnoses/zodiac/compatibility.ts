// ================================================================
// スコア判定
// ================================================================
function scoreToCategory(score: number): CompatibilityCategory {
  if (score >= 80) return 'soulmate';
  if (score >= 65) return 'good';
  if (score >= 50) return 'neutral';
  if (score >= 35) return 'tough';
  return 'opposite';
}

