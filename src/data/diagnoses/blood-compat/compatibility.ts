/**
 * ペアキーをアルファベット順に正規化する
 */
export function formatPairKey(t1: BloodType, t2: BloodType): string {
  return [t1, t2].sort().join('-');
}

type PairKey = string;

export const compatibilityMatrix: Record<PairKey, CompatibilityData> =
  pairsData.reduce(
    (acc, pair) => {
      const key = formatPairKey(pair.type1, pair.type2);
      acc[key] = pair;
      return acc;
    },
    {} as Record<PairKey, CompatibilityData>
  );
