import type { IntelligenceType } from './questions';

export interface FamousPerson {
  name: string;
  /** 公知の根拠または推測明記 */
  note: string;
}

export interface WorkInfo {
  /** 向く職種 10選 */
  jobs: string[];
  /** マネジメントスタイル */
  management: string;
  /** ストレス源 */
  stress: string;
  /** 向く職場環境 */
  environment: string;
}

export interface LoveInfo {
  /** 相性◎タイプ（知能タイプ名） */
  compatibleTypes: string[];
  /** 相性×タイプ（知能タイプ名） */
  challengingTypes: string[];
  /** アプローチ法 */
  approach: string;
  /** 恋愛での傾向 */
  tendency: string;
}

export interface FriendInfo {
  /** 距離感・付き合い方 */
  distance: string;
  /** 友人関係での立ち位置 */
  position: string;
  /** 長続きする友人タイプ */
  goodMatch: string;
}

export interface FamilyInfo {
  /** 親子関係での特性 */
  parent: string;
  /** 夫婦・パートナーとの特性 */
  spouse: string;
}

export interface SchoolInfo {
  /** 学習スタイル */
  style: string;
  /** 向く勉強法 */
  methods: string[];
  /** 得意な教科傾向 */
  subjects: string[];
  /** 進路ヒント */
  career: string;
}

export interface Big5Score {
  /** 開放性（Openness）0-100 */
  O: number;
  /** 誠実性（Conscientiousness）0-100 */
  C: number;
  /** 外向性（Extraversion）0-100 */
  E: number;
  /** 協調性（Agreeableness）0-100 */
  A: number;
  /** 神経症傾向（Neuroticism）0-100 */
  N: number;
}

export interface IntelligenceTypeData {
  /** 日本語タイプ名 */
  name: string;
  /** 英語タイプ名 */
  nameEn: string;
  /** 動物キャライメージ */
  character: string;
  /** キャラ絵文字 */
  emoji: string;
  /** キャッチコピー */
  catchphrase: string;
  /** 概要（200字程度） */
  summary: string;
  /** 強み 5つ */
  strengths: string[];
  /** 弱み 5つ */
  weaknesses: string[];
  /** 成長のヒント 5つ */
  growth: string[];
  /** 仕事・職場 */
  work: WorkInfo;
  /** 恋愛 */
  love: LoveInfo;
  /** 友人 */
  friend: FriendInfo;
  /** 家族 */
  family: FamilyInfo;
  /** 学校・学習 */
  school: SchoolInfo;
  /** 有名人 3-5人（公知の事実ベース） */
  famous: FamousPerson[];
  /** Big5換算スコア（研究傾向値・個人差あり） */
  big5: Big5Score;
  /** テーマカラー（CSS変数名） */
  color: string;
}

