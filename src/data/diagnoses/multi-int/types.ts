/** タイプ名（日本語）からキーを返すヘルパー */
export function getTypeByName(name: string): IntelligenceType | undefined {
  return (Object.entries(types) as [IntelligenceType, IntelligenceTypeData][]).find(
    ([, data]) => data.name === name
  )?.[0];
}

