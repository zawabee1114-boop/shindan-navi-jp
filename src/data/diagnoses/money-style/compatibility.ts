/**
 * 2タイプ間の相性データを取得する
 */
export function getCompatibility(
  typeA: MoneyType,
  typeB: MoneyType
): MoneyCompatibilityData {
  const key = pairKey(typeA, typeB);
  const data = compatibilityTable[key];
  if (!data) {
    // フォールバック
    return {
      score: 60,
      category: '補完し合える関係',
      oneLineSummary: '対話を重ねることで深まる関係',
      detailDescription:
        'それぞれの金銭感覚の違いを理解し合いながら、対話を重ねることで関係が育まれます。',
      moneyManagement: 'お互いの得意・不得意を確認し合い、役割分担を決めることが大切です。',
      conflictArea: '金銭観のズレを率直に話し合うことが、関係の安定への第一歩です。',
      growthPotential: 'お互いの異なる金銭への視点から学び合える関係です。',
      adviceForCouple: '定期的な収支の共有と、目標の話し合いを習慣にしましょう。',
    };
  }
  return data;
}

/**
 * 特定タイプの全他タイプとの相性リストをスコア降順で返す
 */
export function getAllPairsForType(
  typeId: MoneyType
): Array<{ typeId: MoneyType; compatibility: MoneyCompatibilityData }> {
  const allTypes: MoneyType[] = ['avoidance', 'worship', 'status', 'vigilance'];
  return allTypes
    .filter((t) => t !== typeId)
    .map((t) => ({
      typeId: t,
      compatibility: getCompatibility(typeId, t),
    }))
    .sort((a, b) => b.compatibility.score - a.compatibility.score);
}
