/**
 * あるタイプと他の全タイプの相性一覧を取得する
 */
export function getAllCompatibilitiesFor(
  targetType: FriendType,
  allTypes: FriendType[]
): Array<{ otherType: FriendType; data: CompatibilityData }> {
  return allTypes.map((otherType) => ({
    otherType,
    data: getCompatibility(targetType, otherType),
  }));
}
