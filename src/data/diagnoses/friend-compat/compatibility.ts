/**
 * 2つのタイプの相性データを取得する（順不同）
 */
export function getCompatibility(type1: FriendType, type2: FriendType): CompatibilityData {
  // 同タイプチェック
  if (type1 === type2) {
    const found = compatibilityList.find(
      (c) => c.type1 === type1 && c.type2 === type2
    );
    if (found) return found;
  }

  // 順不問で検索
  const found = compatibilityList.find(
    (c) =>
      (c.type1 === type1 && c.type2 === type2) ||
      (c.type1 === type2 && c.type2 === type1)
  );

  if (found) return found;

  // フォールバック（データが見つからない場合）
  return {
    type1,
    type2,
    score: 60,
    category: 'neutral',
    oneLineSummary: '互いを尊重し合える、可能性のある関係',
    detailDescription: '詳細データは準備中です。',
    friendScene: '一緒に過ごす中で、それぞれの良さを発見し合える関係です。',
    cautionPoint: '違いを尊重し合う姿勢が関係を育てます。',
    growthPoint: '互いから異なる視点と強みを学べます。',
  };
}

/**
 * スコアからカテゴリを返す
 */
export function scoreToCategory(score: number): CompatibilityCategory {
  if (score >= 90) return 'soulmate';
  if (score >= 70) return 'good';
  if (score >= 50) return 'neutral';
  if (score >= 30) return 'tough';
  return 'opposite';
}

